package Basicjava;

public class OperatorInJava {
	
	//Operators are used to perform operations on variables and values.
	
	public static void main(String[] args) {
		
//Arithmetic operators: Arithmetic operators are used to perform common mathematical operations.
		
		int integr= 10;
		int integer1=20;
		
		int output =integer1+integr; // + operator is used for Addition
		System.out.println(output);
		
		System.out.println(integer1-integr); // - operator used for subtraction
		
		System.out.println(integr*integer1); // * operator is used for multiplication
		
		System.out.println(integer1/integr); // "/" operator is used for division
		
		System.out.println(integer1%integr);// module % operator is used for reminder
		
		integer1 ++;
		System.out.println(integer1); // ++ is used for increment +1 is added in it
		
		integr --;
		System.out.println(integr); // -- used for decrement -1 
		
		
		
		
		
		
	}

}
